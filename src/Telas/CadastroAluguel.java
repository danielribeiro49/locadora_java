    private void valida(JTextField campo, String msg) throws Exception{
        String info = campo.getText();
        if (info == null || "".equals(info)) {
            campo.requestFocus();
            throw new Exception("Favor digitar "+msg);
        }
    }

    private void btGravarMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btGravarMousePressed
        // TODO add your handling code here:
        String filme = this.tfFilme.getText();
        String cliente = this.tfCliente.getText();
        String data = this.tfData.getText();

        try{
            valida(this.tfFilme, "o filme");
            valida(this.tfCliente, "o cliente");
            valida(this.tfData, "a data");

        Aluguel c = new Aluguel();
        c.setFilme(filme);
        c.setCliente(cliente);
        c.setData(data);

        IAluguelDAO cd = FactoryDAO.getAluguelDAO();
        cd.save(c);

        JOptionPane.showMessageDialog(null, "Registro cadastrado com sucesso!");

        //break;

        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btGravarMousePressed

