    private void acrescentaLinha(){
        DefaultTableModel dtm = (DefaultTableModel)tbFilmes.getModel();
        int linhas = dtm.getRowCount();
        dtm.setRowCount(linhas+1);
        atualizaQuantidadeDeLinhas();
        
    }
    
    private void btMaisUmaLinhaMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btMaisUmaLinhaMousePressed
        
        acrescentaLinha();
        
    }//GEN-LAST:event_btMaisUmaLinhaMousePressed

    private void atualizaQuantidadeDeLinhas(){
        DefaultTableModel dtm = (DefaultTableModel)tbFilmes.getModel();
        int linhas = dtm.getRowCount();
        this.lbLinhas.setText("Linhas: "+linhas);
    }
    
    private void btMenosUmaLinhaMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btMenosUmaLinhaMousePressed
        DefaultTableModel dtm = (DefaultTableModel)tbFilmes.getModel();
        int linhas = dtm.getRowCount();
        if(linhas > 0){
            dtm.setRowCount(linhas-1);
        }
        atualizaQuantidadeDeLinhas();
        
    }//GEN-LAST:event_btMenosUmaLinhaMousePressed
    
