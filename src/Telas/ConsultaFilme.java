    private void incluiInformacoesNoGrid() throws FileNotFoundException, IOException, SQLException, ClassNotFoundException{
        DefaultTableModel dtm = (DefaultTableModel)tbFilmes.getModel();

        Filme c = new Filme();
        IFilmeDAO cd = FactoryDAO.getFilmeDAO();

        List<Filme> list = cd.load();

        dtm.setRowCount(0);
        for(int i = 0; i < list.size(); i++){
            acrescentaLinha();
            Filme bean = list.get(i);
            tbFilmes.setValueAt(bean.getId(), i, COLUNA_ID);
            tbFilmes.setValueAt(bean.getTitulo(), i, COLUNA_TITULO);
            tbFilmes.setValueAt(bean.getValor(), i, COLUNA_VALOR);
          }
    }  
    
    
